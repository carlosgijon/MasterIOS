// No dan lo mismo las divisiones con enteros que con decimales

import UIKit

var x: Int = 5, y: Int = 12
var relacion_ratio = y/x
print("La relacion de ratio con enteros da \(relacion_ratio)")

var fx: Float = 5, fy: Float = 12
var relacion_ratio_con_floats: Float = fy/fx
print("La relacion de ratio con decimales da \(relacion_ratio_con_floats)")
